			<h1>La méthode</h1>

			<p>
			Le but de cette méthode est d’aider les jongleurs à progresser techniquement de manière ludique et agréable en s’appuyant sur une forte notion de rythme. Elle développe la capacité à jouer avec d’autres jongleurs, mais aussi avec d’autres disciplines travaillant avec le tempo comme des musiciens ou des danseurs. Elle permet aussi de mieux comprendre les principes fondamentaux de la jonglerie. Elle concilie apprentissage, créativité et plaisir. Cette méthode se base sur l’étude de partitions qui « sonnent », permettant de travailler des phrases cohérentes et motivantes. Des points techniques sont abordés pas à pas et insérés directement dans les partitions, ces points techniques deviennent des outils permettant de réaliser des partitions et non plus des « figures » isolées. Ces partitions peuvent être apprises, partagées et peuvent devenir un répertoire utile pour s’amuser et progresser, seul ou à plusieurs.
			</p>

			<p>
			Cette méthode est enseigné à l’Académie Fratellini, au CIAM et au CNAC. Différents stages sont organisés tout au long de l’année : <a href=""> En savoir plus </a> 
