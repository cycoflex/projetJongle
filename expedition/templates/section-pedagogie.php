			<h1>La notation</h1>
			<p>
			La <strong>notation trois couches</strong> est un système de notation de la jonglerie simple et complet. Comme son nom l’indique, une ligne de notation comporte 3 couches d’informations : la hauteur et la direction des lancers la trajectoire des lancers autour du corps la position du corps dans l’espace. 
			</p>
			<p>
			Cette notation est un vrai langage de la jonglerie, dans le sens où cette notation est le moyen le plus simple, précis et complet pour parler de la jonglerie. Elle développe la capacité d’analyse. Elle facilite le partage. Elle permet la création d’un répertoire. 
			</p>
			

